import numObj from './index';

describe('Javascript - Medium - 43', () => {
  test('Numbers to Objects', () => {
    expect(numObj([118, 117, 120])).toEqual([
      { 118: 'v' },
      { 117: 'u' },
      { 120: 'x' },
    ]);
    expect(numObj([101, 121, 110, 113, 113, 103])).toEqual([
      { 101: 'e' },
      { 121: 'y' },
      { 110: 'n' },
      { 113: 'q' },
      { 113: 'q' },
      { 103: 'g' },
    ]);
    expect(numObj([118, 103, 110, 109, 104, 106])).toEqual([
      { 118: 'v' },
      { 103: 'g' },
      { 110: 'n' },
      { 109: 'm' },
      { 104: 'h' },
      { 106: 'j' },
    ]);
    expect(numObj([107, 99, 110, 107, 118, 106, 112, 102])).toEqual([
      { 107: 'k' },
      { 99: 'c' },
      { 110: 'n' },
      { 107: 'k' },
      { 118: 'v' },
      { 106: 'j' },
      { 112: 'p' },
      { 102: 'f' },
    ]);
    expect(numObj([100, 100, 116, 105, 117, 121])).toEqual([
      { 100: 'd' },
      { 100: 'd' },
      { 116: 't' },
      { 105: 'i' },
      { 117: 'u' },
      { 121: 'y' },
    ]);
  });
});
